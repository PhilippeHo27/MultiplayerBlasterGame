void ABlasterPlayerState::SetTeam(ETeam ETeamToSet)
{
	Team = ETeamToSet;
	ABlasterCharacter* BCharacter = Cast<ABlasterCharacter>(GetPawn());
	if (BCharacter)
	{
		BCharacter->SetTeamColor(Team);
	}
}

